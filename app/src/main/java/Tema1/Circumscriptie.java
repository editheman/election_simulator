package Tema1;
public class Circumscriptie {
    public String nume;
    public String regiune;

    public Circumscriptie(String nume, String regiune) {
        this.nume = nume;
        this.regiune = regiune;
    }

    public String getNume() {
        return nume;
    }
    public String getRegiune() {
        return regiune;
    }
}
